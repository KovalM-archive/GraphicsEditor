package kmv.editor.graphics.drawing.model;

/**
 * @author Uladzislau Jum
 */
public class Parabola implements GeometryObject {
    private int x;
    private int y;
    private int p;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }
}
