package kmv.editor.graphics.drawing.model;

/**
 * @author Uladzislau Jum
 */
public class Hyperbola implements GeometryObject {
    private int x;
    private int y;
    private int a;
    private int b;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
